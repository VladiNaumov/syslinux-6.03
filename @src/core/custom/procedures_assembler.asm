[bits 16]
[org 0x7c00]

start:
    cli                     ; Отключение прерываний
    mov ax, 0x07c0          ; Установка сегмента данных
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x7c00          ; Установка указателя стека

    ; Вызов первой процедуры
    call first_procedure

    ; Вызов второй процедуры
    call second_procedure

    ; Завершение работы (например, бесконечный цикл)
hang:
    jmp hang

; Первая процедура
first_procedure:
    mov ah, 0x0e            ; Функция печати символа
    mov al, 'A'             ; Символ для печати
    mov bh, 0x00            ; Страница (0)
    mov bl, 0x07            ; Атрибут (белый текст на черном фоне)
    int 0x10                ; Вызов прерывания BIOS
    ret                     ; Возврат из процедуры

; Вторая процедура
second_procedure:
    mov ah, 0x0e            ; Функция печати символа
    mov al, 'B'             ; Символ для печати
    mov bh, 0x00            ; Страница (0)
    mov bl, 0x07            ; Атрибут (белый текст на черном фоне)
    int 0x10                ; Вызов прерывания BIOS
    ret                     ; Возврат из процедуры

times 510 - ($ - $$) db 0
dw 0xaa55
